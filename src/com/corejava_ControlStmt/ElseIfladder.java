package com.corejava_ControlStmt;

public class ElseIfladder {
public static void main(String[] args) {
	
	int  a=90,b=80,c=100;
	
	if (a==b) {
		System.out.println("if block");
	}
	else if (a>=b) {
		System.out.println("else if-1");
	}
	else if (a>=c) {
		System.out.println("else if-2");
	}else {
		
		System.out.println("");
	}
	
}
}
