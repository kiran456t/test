package com.corejava_ControlStmt;

public class SwitchDemo {
	public static void main(String[] args) {
		
		String name="cource";
		switch (name) {
		case "cource":
			System.out.println("java with selenium in real time");
			//break;
		case "onlineclass":
			System.out.println("online class");
			//break;
		case "kiran":
			System.out.println("welcome to kiran");
			//break;
			
		default:
			System.out.println("default case");
			//break;
		}
		System.out.println("out of switch case");
		System.out.println("fdfsfs");
		
	}

}
